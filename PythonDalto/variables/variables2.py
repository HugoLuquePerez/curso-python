nombre = "Hugo"
bienvenida = "Hola bienvenido " + nombre + " como estas"

""" como saber si una variable contiene una palabra """
""" con (in) para saber si contiene esa palabra """

print("Hola" in bienvenida)

""" con (not in) para saber si no la contiene """

print("welcome" not in bienvenida)

