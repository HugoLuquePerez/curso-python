""" variables: Es como el contenedor de información 
Las variables se declaran y se definen, tambien se pueden modificar """

a = 2
b = 3
c = a + b
print(c)

""" Concatenacion de variables y cadenas """  
""" contenacion entre los mismo tipos de datos """

nombre = "Hugo"
bienvenida = "Hola bienvenido " + nombre + " como estas"
print(bienvenida)

""" para concatenar con diferentes tipos de datos vamosa usar el f string """
edad = 18
bienvenida2 = f"hola tienes {edad} y puedes pasar ¡Disfrute!"
print(bienvenida2)

""" como eliminar la información de la variable """

del bienvenida2
print(bienvenida2)

